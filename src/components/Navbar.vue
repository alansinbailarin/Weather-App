<script setup lang="ts">
import { ref, onMounted } from "vue";
import type { weather } from "@/interfaces/fiveDaysWeather";

const dataCurrent = ref<weather>({} as weather);
let loading = ref<boolean>(true);

let unit = "metric";
const API_key = "21a45b728b91cea07a38b918145aafc4";

const getWeather = () => {
  navigator.geolocation.getCurrentPosition((position) => {
    let longitud = position.coords.longitude;
    let latitud = position.coords.latitude;

    const getCurrentWeather = `https://api.openweathermap.org/data/2.5/weather?lat=${latitud}&lang=es&lon=${longitud}&appid=${API_key}&units=${unit}`;

    fetch(getCurrentWeather)
      .then((res) => {
        return res.json();
      })
      .then((data) => {
        loading.value = true;
        try {
          dataCurrent.value = data;
        } catch (error) {
          console.log(error);
        } finally {
          loading.value = false;
        }
      });
  });
};

if (
  localStorage.theme === "dark" ||
  (!("theme" in localStorage) &&
    window.matchMedia("(prefers-color-scheme: dark)").matches)
) {
  document.documentElement.classList.add("dark");
} else {
  document.documentElement.classList.remove("dark");
}
// const toggleDarkMode = ref(document.documentElement.className === "dark");
const toggleDarkMode = ref(document.documentElement.className.includes("dark"));
const changeDarkMode = () => {
  toggleDarkMode.value = document.documentElement.classList.toggle("dark");
  toggleDarkMode.value
    ? (localStorage.theme = "dark")
    : (localStorage.theme = "light");
};

onMounted(() => {
  getWeather();
});
</script>
